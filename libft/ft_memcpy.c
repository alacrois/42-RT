#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char		*c1;
	const char	*c2;

	if (n != 0)
	{
		c1 = (char *)s1;
		c2 = (char *)s2;
		while (n > 0)
		{
			*c1 = *c2;
			c1++;
			c2++;
			n--;
		}
	}
	return (s1);
}
